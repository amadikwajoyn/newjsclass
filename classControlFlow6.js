// switch statement
 const grades = "E";

switch (grades) {
    case 'A':
        console.log('You got an A!');
        break;
    case 'B':
        console.log('You got a B!');
        break;
    case 'C':
        console.log('You got a C!');break;
    case 'D':
        console.log('You got a D!');
        break;
    case 'E':
        console.log('You got a E!');
        break;
    default:
        console.log('Not a valid grade')
        break;
}